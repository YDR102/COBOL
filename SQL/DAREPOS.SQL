DROP TABLE IF EXISTS DAREPOS;
COMMIT;
-- HACE EFECTIVO EL DROP

CREATE TABLE DAREPOS
(
    NOMBRE_PGM CHAR(08) NOT NULL,
    ESTADO CHAR(02) NOT NULL,
    VALOR_CLAVE VARCHAR(1000) NOT NULL,
    CONSTRAINT PK_NOMBRE_PGM PRIMARY KEY (NOMBRE_PGM)
);

CREATE UNIQUE INDEX IDX_NOMBRE_PGM ON DAREPOS (NOMBRE_PGM);
